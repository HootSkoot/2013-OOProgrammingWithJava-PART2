public class Plane {
    private String name;
    private int capacity;

    public Plane(String name, int capacity) {
        this.name = name;
        this.capacity = capacity;
    }

    public String getName() {
        return name;
    }

    public int getCapacity() {
        return capacity;
    }

    @Override
    public String toString() {
        return name + " (" + capacity + " ppl)"; //To change body of generated methods, choose Tools | Templates.
    }
    
}
